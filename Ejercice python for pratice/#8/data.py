
dateUsser = ["Yhonatan", "Victor", "Fernanda", "Emely"]
print(dateUsser)

#Agregar elemento al final de la list
dateUsser.append("Victor")
print(dateUsser)

#pop() elimina y retorna un elemento de una lista.
dateUsser.pop()
print(dateUsser)

#Para verificar que un elemento se encuentra en la lista usar (in)
"Yhonatan" in dateUsser
print(dateUsser)